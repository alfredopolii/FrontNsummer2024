//Rumus : BMI = berat / (tinggi * tinggi), berat dalam kg dan tinggi dalam meter
//170 cm = 1.7 m
//Fungsi tersebut menerima 2 input yaitu berat dan tinggi
//Dan mengembalikan output berupa BMI


